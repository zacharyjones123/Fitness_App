        infoip.setText(server.getIpAddress() + ":" + server.getPort());

        Button tuesday_BackEdit = (Button) findViewById(R.id.tuesdayBack_button);
        tuesday_BackEdit.setOnClickListener(new View.OnClickListener() {
            public void onClick(View v) {
                Intent intent = new Intent(TuesdayActivity.this, WeightTrainingActivity.class);
                startActivity(intent);
            }
        });

        FloatingActionButton fab = (FloatingActionButton) findViewById(R.id.fab);
